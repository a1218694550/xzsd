package com.xzsd.app.customer.goodsDetail.entity;

public class ImageInfo {
    private String evaluateCode;
    private String imgUrl;
    private String sortOrdinal;

    public String getEvaluateCode() {
        return evaluateCode;
    }

    public void setEvaluateCode(String evaluateCode) {
        this.evaluateCode = evaluateCode;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getSortOrdinal() {
        return sortOrdinal;
    }

    public void setSortOrdinal(String sortOrdinal) {
        this.sortOrdinal = sortOrdinal;
    }
}
